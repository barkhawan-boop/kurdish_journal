const state = {
  lang: "en",
  catalog: null,
  results: [],
  latest: [],
  selectedArticle: null
};

const $ = (selector) => document.querySelector(selector);
const resultsEl = $("#results");
const template = $("#result-template");

function t(key) {
  return translations[state.lang][key] || translations.en[key] || key;
}

function applyLanguage(lang) {
  state.lang = lang;
  document.documentElement.lang = lang;
  document.body.classList.toggle("rtl", lang === "ku" || lang === "ar");
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll(".language-switcher button").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });
  renderLatestArticles();
  renderResults();
}

function localizedInstitution(institution) {
  if (!institution) return "";
  if (state.lang === "ku") return institution.name_ku || institution.name_en;
  if (state.lang === "ar") return institution.name_ar || institution.name_en;
  return institution.name_en;
}

function localizedTitle(article) {
  if (state.lang === "ku") return article.title_ku || article.title;
  if (state.lang === "ar") return article.title_ar || article.title;
  return article.title;
}

async function loadCatalog() {
  await loadStats();

  const response = await fetch("/api/catalog", { cache: "no-store" });
  if (!response.ok) {
    throw new Error(`Catalog request failed with ${response.status}`);
  }
  const catalog = await response.json();
  state.catalog = {
    metadata: catalog.metadata || {},
    institutions: Array.isArray(catalog.institutions) ? catalog.institutions : [],
    journals: Array.isArray(catalog.journals) ? catalog.journals : [],
    subjects: Array.isArray(catalog.subjects) ? catalog.subjects : [],
    institution_count: Number(catalog.institution_count || 0),
    journal_count: Number(catalog.journal_count || 0),
    source_count: Number(catalog.source_count || 0),
    article_count: Number(catalog.article_count || 0)
  };

  updateStats({
    institution_count: state.catalog.institution_count || state.catalog.institutions.length,
    journal_count: state.catalog.journal_count || state.catalog.journals.length,
    source_count: 0,
    article_count: state.catalog.article_count
  });

  const subjectFilter = $("#subject-filter");
  subjectFilter.querySelectorAll("option:not([value='all'])").forEach((option) => option.remove());
  state.catalog.subjects.forEach((subject) => {
    const option = document.createElement("option");
    option.value = typeof subject === "string" ? subject : subject.value;
    option.textContent = typeof subject === "string" ? subject : subject.label;
    subjectFilter.appendChild(option);
  });
  await loadLatestArticles();
}

async function loadStats() {
  const response = await fetch("/api/stats", { cache: "no-store" });
  if (!response.ok) {
    throw new Error(`Stats request failed with ${response.status}`);
  }
  const stats = await response.json();
  updateStats(stats);
  return stats;
}

function updateStats(stats) {
  const institutions = Number(stats.institution_count || 0);
  const journals = Number(stats.journal_count || 0);
  const articles = Number(stats.article_count || 0);
  $("#institution-count").textContent = institutions.toLocaleString();
  $("#journal-count").textContent = journals.toLocaleString();
  $("#article-count").textContent = articles.toLocaleString();
}

async function loadLatestArticles() {
  const response = await fetch("/api/latest?limit=12", { cache: "no-store" });
  if (!response.ok) {
    throw new Error(`Latest articles request failed with ${response.status}`);
  }
  const payload = await response.json();
  state.latest = Array.isArray(payload.results) ? payload.results : [];
  renderLatestArticles();
}

function renderLatestArticles() {
  const list = $("#latest-articles");
  if (!list) return;
  list.innerHTML = "";
  state.latest.forEach((article) => {
    const item = document.createElement("button");
    item.className = "latest-article";
    item.type = "button";
    const title = document.createElement("strong");
    const meta = document.createElement("span");
    title.textContent = localizedTitle(article);
    meta.textContent = `${article.year || "n.d."} · ${article.journal?.title || "Journal"}`;
    item.append(title, meta);
    item.addEventListener("click", () => {
      $("#query").value = article.title;
      runSearch();
      document.querySelector(".results-panel")?.scrollIntoView({ behavior: "smooth", block: "start" });
    });
    list.appendChild(item);
  });
}

async function refreshArticles() {
  const button = $("#refresh-articles-button");
  const status = $("#refresh-status");
  button.disabled = true;
  status.textContent = t("refreshingArticles");
  try {
    const response = await fetch("/api/refresh-articles", { method: "POST" });
    const payload = await response.json();
    if (!response.ok) {
      throw new Error(payload.error || "Refresh failed");
    }
    status.textContent = `${t("refreshComplete")} ${payload.imported} new articles.`;
    await loadCatalog();
    await runSearch();
  } catch (error) {
    console.error(error);
    status.textContent = t("refreshFailed");
  } finally {
    button.disabled = false;
  }
}

async function runSearch() {
  const q = $("#query").value.trim();
  const type = $("#type-filter").value;
  const subject = $("#subject-filter").value;
  const index = $("#index-filter").value;
  const params = new URLSearchParams({ q, type, subject, index });
  const response = await fetch(`/api/search?${params}`);
  const payload = await response.json();
  state.results = payload.results;
  $("#result-count").textContent = `${payload.count} ${state.lang === "en" ? "results" : ""}`;
  renderResults();
}

function renderResults() {
  if (!resultsEl) return;
  resultsEl.innerHTML = "";
  if (!state.results.length) {
    const empty = document.createElement("div");
    empty.className = "empty";
    empty.textContent = t("noResults");
    resultsEl.appendChild(empty);
    return;
  }

  const style = $("#citation-style").value;
  state.results.forEach((item) => {
    if (item.kind === "source") {
      renderSourceResult(item);
      return;
    }

    const article = item;
    const node = template.content.cloneNode(true);
    const card = node.querySelector(".result-card");
    const citation = article.citations[style] || article.citations.apa;
    const pdfLink = node.querySelector(".pdf-link");
    const summary = article.display_summary || article.summary || article.abstract;

    node.querySelector(".result-meta").textContent = `${article.authors.join(", ")} · ${article.year} · ${t("score")} ${article.score}`;
    node.querySelector("h3").textContent = article.title;
    node.querySelector(".localized-title").textContent = localizedTitle(article);
    node.querySelector(".summary").textContent = summary;
    node.querySelector(".journal-name").textContent = article.journal.title;
    node.querySelector(".institution-name").textContent = localizedInstitution(article.institution);
    node.querySelector(".impact-factor").textContent = article.journal.impact_factor ?? "Not verified";
    node.querySelector(".indexing-status").textContent = (article.journal.indexing || []).join(" / ") || "Not verified";
    node.querySelector(".ranking").textContent = article.journal.ranking || "Not verified";
    node.querySelector(".citation").textContent = citation;
    node.querySelector(".copy-citation").textContent = t("copyCitation");
    node.querySelector(".copy-summary").textContent = t("summarizePdf");

    const tags = node.querySelector(".tags");
    article.keywords.slice(0, 8).forEach((keyword) => {
      const tag = document.createElement("span");
      tag.textContent = keyword;
      tags.appendChild(tag);
    });

    if (article.pdf_url) {
      pdfLink.href = article.pdf_url;
      pdfLink.textContent = "PDF";
    } else if (article.url) {
      pdfLink.href = article.url;
      pdfLink.classList.add("secondary-link");
      pdfLink.textContent = t("articleLink");
    } else {
      pdfLink.href = article.pdf_search_url;
      pdfLink.classList.add("secondary-link");
      pdfLink.textContent = t("findPdf");
    }

    node.querySelector(".copy-citation").addEventListener("click", (event) => {
      copyText(citation, event.currentTarget);
    });
    node.querySelector(".copy-summary").addEventListener("click", () => {
      preparePdfSummary(article);
    });

    resultsEl.appendChild(card);
  });
}

function preparePdfSummary(article) {
  state.selectedArticle = article;
  const keywordInput = $("#pdf-keyword");
  const output = $("#pdf-summary-output");
  const tool = $("#pdf-summary-title");

  keywordInput.value = article.title;
  output.value = "Ready. Click Summarise PDF to create a summary from the uploaded PDF or from this article metadata.";

  tool.scrollIntoView({ behavior: "smooth", block: "start" });
}

function renderSourceResult(source) {
  const card = document.createElement("article");
  card.className = "result-card source-card";

  const main = document.createElement("div");
  main.className = "result-main";

  const meta = document.createElement("div");
  meta.className = "result-meta";
  meta.textContent = `${t("sourceLink")} · ${source.institution || "Directory"} · ${t("score")} ${source.score}`;

  const title = document.createElement("h3");
  title.textContent = source.title;

  const summary = document.createElement("p");
  summary.className = "summary";
  summary.textContent = source.summary || source.url;

  const tags = document.createElement("div");
  tags.className = "tags";
  source.subjects.slice(0, 8).forEach((subject) => {
    const tag = document.createElement("span");
    tag.textContent = subject;
    tags.appendChild(tag);
  });

  const actions = document.createElement("div");
  actions.className = "actions";
  if (source.url) {
    const link = document.createElement("a");
    link.href = source.url;
    link.target = "_blank";
    link.rel = "noreferrer";
    link.textContent = t("openSource");
    actions.appendChild(link);
  } else {
    const missing = document.createElement("span");
    missing.className = "missing-link";
    missing.textContent = t("linkNeedsVerification");
    actions.appendChild(missing);
  }

  main.append(meta, title, summary, tags, actions);

  const metrics = document.createElement("dl");
  metrics.className = "journal-metrics";
  const institutionBlock = document.createElement("div");
  const institutionLabel = document.createElement("dt");
  const institutionValue = document.createElement("dd");
  institutionLabel.textContent = "Institution";
  institutionValue.textContent = source.institution || "Directory";
  institutionBlock.append(institutionLabel, institutionValue);

  const urlBlock = document.createElement("div");
  const urlLabel = document.createElement("dt");
  const urlValue = document.createElement("dd");
  urlLabel.textContent = "URL";
  urlValue.textContent = source.url || t("linkNeedsVerification");
  urlBlock.append(urlLabel, urlValue);
  metrics.append(institutionBlock, urlBlock);

  card.append(main, metrics);
  resultsEl.appendChild(card);
}

async function copyText(text, button) {
  await navigator.clipboard.writeText(text);
  const oldText = button.textContent;
  button.textContent = t("copied");
  setTimeout(() => {
    button.textContent = oldText;
  }, 1200);
}

async function paraphrase() {
  const text = $("#paraphrase-input").value;
  const tone = $("#paraphrase-tone").value;
  const response = await fetch("/api/paraphrase", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ text, tone })
  });
  const payload = await response.json();
  $("#paraphrase-output").value = payload.paraphrased;
}

async function summarizePdf() {
  const fileInput = $("#pdf-file");
  const output = $("#pdf-summary-output");
  if (!fileInput.files.length) {
    if (state.selectedArticle) {
      output.value = t("summarising");
      const response = await fetch("/api/summarize-article", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(state.selectedArticle)
      });
      const payload = await response.json();
      output.value = payload.summary || payload.error || "Article summary failed.";
      return;
    }
    output.value = t("choosePdf");
    return;
  }

  output.value = t("summarising");
  const formData = new FormData();
  formData.append("pdf", fileInput.files[0]);
  formData.append("keyword", $("#pdf-keyword").value.trim());

  const response = await fetch("/api/summarize-pdf", {
    method: "POST",
    body: formData
  });
  const payload = await response.json();
  if (!response.ok) {
    output.value = payload.error || "PDF summarisation failed.";
    return;
  }
  output.value = `${payload.summary}\n\nExtracted text preview:\n${payload.extracted_preview}`;
}

async function exportSummaryPdf() {
  const text = $("#pdf-summary-output").value.trim();
  if (!text) {
    $("#pdf-summary-output").value = "Create a summary first, then export it.";
    return;
  }
  const response = await fetch("/api/export-summary-pdf", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ text })
  });
  if (!response.ok) {
    const payload = await response.json();
    $("#pdf-summary-output").value = payload.error || "Export failed.";
    return;
  }
  const blob = await response.blob();
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = "research-summary.pdf";
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

